package ex_07_Increment_Decrement_Op;

public class Lab075076_Exp1
{
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a++ + ++a);
        System.out.println(a);

        int b = 10;
        System.out.println(++b + ++b);
        System.out.println(b);
    }
}
